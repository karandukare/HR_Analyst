DROP TABLE IF EXISTS hr_analyst;

CREATE TABLE hr_analyst(
    employee_id INT,
    department TEXT,
    region TEXT,
    education TEXT,
    gender TEXT,
    recruitment_channel TEXT,
    no_of_trainings INT,
    age INT,
    previous_year_rating INT,
    length_of_service INT,
    KPIs_met_more_than_80 INT,
    awards_won INT,
    avg_training_score INT
);
